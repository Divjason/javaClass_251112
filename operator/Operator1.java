package operator;

public class Operator1 {
    public static void main(String[] args) {
        int a = 5;
        int b = 2;

        int sum = a + b;
        System.out.println(sum);

        int diff = a - b;
        System.out.println(diff);

        int multi = a * b;
        System.out.println(multi);

        int div = a / b;
        System.out.println(div);

        int mod = a % b;
        System.out.println(mod);
        // 짝수 & 홀수 판별을 위해서 > 2 -> 0
        // 소수 값을 판별하기 위해서
    }
}
